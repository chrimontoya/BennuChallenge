    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        // TODO add your handling code here:
        //Método que ordena números y almacena en un nuevo archivo
        number.orderNumbers();
        String name = JOptionPane.showInputDialog("Ingrese el nombre del archivo a crear");
        path=number.createFile(name);
        number.writeFile(path, number.getListNumber());
        JOptionPane.showMessageDialog(null, "Escritura completada");
        System.out.println("Se ha creado el archivo "+name+" en la ruta: "+path);
        JOptionPane.showMessageDialog(null, "Lista ordenada");
    }//GEN-LAST:event_jMenuItem3ActionPerformed

