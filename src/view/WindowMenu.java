    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        //Metodo que genera números aleatorios recibiendo el largo por parámetro
        String sizeNumber = JOptionPane.showInputDialog("Ingrese la cantidad de números a generar");
        List currentList=number.randomNumber(Integer.parseInt(sizeNumber));
        //Se guarda la lista actual en la instancia number, recibiendo la lista como parámetro
        number.setListNumber(currentList);
        String name = JOptionPane.showInputDialog("Ingrese el nombre del archivo");
        path =number.createFile(name);
        number.writeFile(path, currentList);
        JOptionPane.showMessageDialog(null, "Escritura completada");
        System.out.println("Se ha creado el archivo "+name+" en la ruta: "+path);   
    }//GEN-LAST:event_jMenuItem1ActionPerformed

